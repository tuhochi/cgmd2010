package at.ac.tuwien.cg.cgmd.bifth2010.level55;

public class Position {
	
	int x;
	int y;
	
	public Position(int _x, int _y) {
		x=_x;
		y=_y;
	}
	
	public void setPosition(int _x, int _y) {
		x=_x;
		y=_y;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
}
